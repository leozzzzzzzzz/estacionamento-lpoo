    private void btnRemoverVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverVeiculoActionPerformed
        Veiculo veiculoSel = lstVeiculos.getSelectedValue();
        if (veiculoSel != null) {
            System.out.println("Veiculo: " + veiculoSel.getId());
            try {
                jpa.conexaoAberta();

                int delOp = JOptionPane.showConfirmDialog(this,
                        "Tem certeza que deseja remover " + veiculoSel.getPlaca()+ "?");
                if (delOp == JOptionPane.YES_OPTION) {
                    jpa.remover(veiculoSel);
                }

                jpa.fecharConexao();
                carregarVeiculosCadastrados();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Erro ao remover veiculo " + veiculoSel + "\n" + e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um veiculo para remover");
        }
    }//GEN-LAST:event_btnRemoverVeiculoActionPerformed
 
                                     
    
    private void btnEditarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarVeiculoActionPerformed
        Veiculo veiculoSel = lstVeiculos.getSelectedValue();
        if (veiculoSel != null) {
            TelaCadastroVeiculo telaEdt = new TelaCadastroVeiculo(this, rootPaneCheckingEnabled);
            telaEdt.setVeiculo(veiculoSel);
            telaEdt.setVisible(true);
            
            
            carregarVeiculosCadastrados();
            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um veiculo para Editar");
        }
    }//GEN-LAST:event_btnEditarVeiculoActionPerformed

